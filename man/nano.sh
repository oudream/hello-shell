
nano <file_path>

### 保存文件：
# 使用快捷键：按下 Ctrl + O，然后按下 Enter 来保存文件。
# 或者，可以在编辑器底部看到命令提示符 ^O WriteOut，输入 ^O 后按下 Enter。

### 退出 Nano：
#使用快捷键：按下 Ctrl + X 来退出 Nano。
#如果文件已更改但尚未保存，Nano 将提示你是否要保存更改。

### 移动光标：
#使用方向键：使用方向键上下左右移动光标。
#使用快捷键：按下 Ctrl + F 向前移动，Ctrl + B 向后移动，Ctrl + N 向下移动一行，Ctrl + P 向上移动一行。

### 剪切、复制和粘贴：
#使用快捷键：
#剪切：按下 Ctrl + K
#复制：按下 Ctrl + ^（即 Ctrl + Shift + 6）
#粘贴：按下 Ctrl + U

### 搜索和替换：
#搜索：按下 Ctrl + W，然后输入要搜索的文本，按下 Enter。
#替换：按下 Ctrl + \，然后输入要替换的文本和新的文本，按下 Enter。

### 撤消和重做：
#撤消：按下 Ctrl + _（即 Ctrl + Shift + -）
#重做：按下 Alt + _（即 Alt + Shift + -）
