
# 列出块设备，磁盘
lsblk

# 列出串口设备：
dmesg | grep tty

# dmesg：该命令用于显示系统启动过程中内核产生的消息。它提供了有关硬件、驱动程序和内核事件的信息，包括错误、警告和其他系统消息。
#
# dmeventd：这是一个守护进程，用于监视和管理设备映射器事件。设备映射器（Device Mapper）是 Linux 内核中的一个子系统，用于在逻辑卷管理器（LVM）和其他存储技术之上创建逻辑卷。
#
# dmidecode：该命令用于从系统的 DMI（Desktop Management Interface）数据结构中提取硬件信息。它可以显示关于系统硬件、BIOS、主板、内存、处理器等方面的详细信息。
#
# dmsetup：这是一个用于管理设备映射器的命令行工具。它允许你创建、修改和删除设备映射器设备，以及查询有关这些设备的信息。
#
# dmstats：这是一个用于显示设备映射器统计信息的工具。它可以提供有关设备映射器设备的读取、写入、映射和映射失败的统计数据。

